#include<stdio.h>

int main(){
    printf("time it up");

    int red=4;
    printf("%d",red);
    // a for loop used to execute a block of code a number of times 
    // for(initialization, condition, de/incrementation)
    for(int i=red; i<=4; i--){
        printf("%d\n",i);
    }
    return 0;
}

